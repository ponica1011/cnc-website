function translateValue(value, lang) {
  if (lang === "zh") return value;
  const langDict = translations[lang] || {};
  if (langDict[value]) return langDict[value];
  if (lang === "en" && translations.en?.[value]) return translations.en[value];

  let translated = value;
  const keys = Object.keys(langDict)
    .filter((key) => key && key !== value && translated.includes(key))
    .sort((a, b) => b.length - a.length);

  keys.forEach((key) => {
    const replacement = langDict[key];
    if (replacement) translated = translated.split(key).join(replacement);
  });

  if (lang === "en" && translated === value) {
    const enKeys = Object.keys(translations.en)
      .filter((key) => key && key !== value && translated.includes(key))
      .sort((a, b) => b.length - a.length);
    enKeys.forEach((key) => {
      translated = translated.split(key).join(translations.en[key]);
    });
  }

  return translated;
}

function translatePage(lang) {
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  if (!document.body.dataset.originalTitle) document.body.dataset.originalTitle = document.title;
  document.title = translateValue(document.body.dataset.originalTitle, lang);
  document.querySelectorAll(".language-select").forEach((select) => {
    select.value = lang;
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      const parent = node.parentElement;
      if (!parent || parent.closest("script, style, .language-select")) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    if (!originalText.has(node)) originalText.set(node, node.nodeValue);
    const original = originalText.get(node);
    const leading = original.match(/^\s*/)[0];
    const trailing = original.match(/\s*$/)[0];
    const key = original.trim();
    node.nodeValue = `${leading}${translateValue(key, lang)}${trailing}`;
  });

  document.querySelectorAll("[placeholder], [aria-label]").forEach((element) => {
    ["placeholder", "aria-label"].forEach((attr) => {
      if (!element.hasAttribute(attr)) return;
      const dataKey = `original${attr.replace("-", "")}`;
      if (!element.dataset[dataKey]) element.dataset[dataKey] = element.getAttribute(attr);
      element.setAttribute(attr, translateValue(element.dataset[dataKey], lang));
    });
  });
}

